package es.jdbc.sql.search.functions;

import java.math.BigDecimal;
import java.util.ArrayList;

import es.jdbc.sql.search.ISelectItem;
import es.jdbc.sql.search.sources.IValue;

public class InnerSize extends AbstractCommonFunction {
    public int createSize() {
        IValue sizeValue = (IValue)this.parameters.get(0);
        return new BigDecimal((Long) sizeValue.getValue()).intValue();
    }
    /**
     * コンストラクター.
     */
    public InnerSize() {
        this.parameters = new ArrayList<ISelectItem>();
    }
}
